package javaday1;

public class swap {
    public static void main(String[] args) {
        int a=4;
        int b=5;
        int temp;
        temp=a;
        a=b;
        b=temp;
        System.out.println("a after swapping: "+a);
        System.out.println("b after swapping: "+b);
        
    }
    
}
